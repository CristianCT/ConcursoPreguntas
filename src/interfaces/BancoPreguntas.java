    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.seleccion){
            
            this.concurso.setAcumulado(this.concurso.getAcumulado() + this.concurso.getRondas().get(this.indexRonda).getPremio().getValor());
            this.indexRonda++;
            this.mostrarPregunta();
            this.seleccion = false;
            if(this.indexRonda == this.concurso.getRondas().size()){
                this.jLabel3.setText("Puntos: " + this.concurso.getAcumulado());
                this.concurso.saveResults();
                JOptionPane.showMessageDialog(null, "Felicidades, Obtuviste el Premio Gordo " + this.concurso.getAcumulado()+ "pts");
                this.setVisible(false);
                this.opciones.setVisible(true);
                this.concurso.setAcumulado(0);
            }
        } else {
            this.concurso.setAcumulado(0); 
            this.concurso.saveResults();
            JOptionPane.showMessageDialog(null, "GAME OVER");
            this.setVisible(false);
            this.opciones.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

