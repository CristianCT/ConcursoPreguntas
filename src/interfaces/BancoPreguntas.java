    private void mostrarPregunta(){
        if(this.indexRonda != this.concurso.getRondas().size()){
            this.categoria = this.concurso.getRondas().get(indexRonda).getCategoria();
            this.pregunta = this.categoria.getRandomPregunta();
            this.jLabel5.setText(this.jugador.getNombreUsuario());
            this.jLabel3.setText("Puntos: " + this.concurso.getAcumulado());
            this.jLabel4.setText("Nivel: " + this.categoria.getTextDificultad());
            this.jLabel2.setText((this.indexRonda + 1) + ". " + this.categoria.getRandomPregunta().getContenido());
            this.jRadioButton1.setText("A. " + this.pregunta.getRespuestas().get(0).getContenido());
            this.jRadioButton2.setText("B. " + this.pregunta.getRespuestas().get(1).getContenido());
            this.jRadioButton3.setText("C. " + this.pregunta.getRespuestas().get(2).getContenido());
            this.jRadioButton4.setText("D. " + this.pregunta.getRespuestas().get(3).getContenido());
        }
    }
